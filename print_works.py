    def mask_secret_word(self, secret_word):
        for item in secret_word:
            self.masked_word = self.masked_word + '_  '

    def unmask_secret_letter(self, index=0, guess=''):
        mylist = list(self.masked_word)
        mylist[index*3] = guess
        for item in mylist:
            self.masked_word = ''.join(mylist)
